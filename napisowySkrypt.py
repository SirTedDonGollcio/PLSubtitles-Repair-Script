nazwa = input()

plik = open(nazwa,encoding="utf-8")

tekst = plik.read()
tekst = tekst.replace("³","ł")
tekst = tekst.replace("ñ","ń")
tekst = tekst.replace("ê","ę")
tekst = tekst.replace("æ","ć")
tekst = tekst.replace("¿","ż")
tekst = tekst.replace("œ","ś")
tekst = tekst.replace("¹","ą")
tekst = tekst.replace("Ÿ","ź")
tekst = tekst.replace("Æ","Ć")
tekst = tekst.replace("¯","Ż")
tekst = tekst.replace("Œ","Ś")
plik.close()
plik = open(nazwa,'w',encoding="utf-8")
plik.write(tekst)
plik.close()
